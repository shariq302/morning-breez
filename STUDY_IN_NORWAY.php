                                <h2>Why Study In Norway</h2>
                                <br>

                                <div id="content-above"><p><span style="font-weight: 400;">A Scandinavian country, Norway is well developed and forward thinking. Known for its Viking ancestors, Norway has a long and fascinating history. With beautiful fjords, the famous Aurora Borealis (Northern Lights) and a great reputation for research, Norway is perfect for any international student.</span>
                                    </p>

                                    <h3 id="1"><span style="font-weight: 400;">Why Study in Norway?</span></h3>

                                    <p><span style="font-weight: 400;">A progressive country, Norway strives to provide students with high quality education. There are three types of public higher education institution in Norway. These are universities, university colleges and specialist university colleges. There are also many private higher education institutions. The Norwegian education system follows the Bologna Process, meaning that you can gain a bachelor’s, master’s and doctoral degree.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">There are 4 of Norway’s universities in the 2019 QS World University Rankings top 500. The highest ranked is the University of Oslo, which is placed at 135th. The next highest ranked is the University of Bergen, which is placed at 171st.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Couple the fantastic quality of universities in Norway with the gorgeous scenery and high standards of living, and you have a great study environment.</span>
                                    </p>

                                    <h3 id="2"><span style="font-weight: 400;">About Norway</span></h3>

                                    <p><span style="font-weight: 400;">Officially the Kingdom of Norway, the country is located in Northwestern Europe. It is home to over 5.3 million people, and this population is incredibly diverse. As well as the mainland area of Norway, the Kingdom also includes the island of Jan Mayen and the archipelago of Svalbard. Norway shares land borders with Sweden, Finland and Russia, and has a vast coastline. Due Norway’s proximity to the Gulf Stream, it benefits from a mild climate.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Despite not being a member state, Norway maintains a close relationship with the European Union. It was also a founding member of the United Nations, NATO and the Council of Europe, as well as being part of the European Economic Area and the Schengen Area. Outside of the Middle East, Norway is the world’s largest producer of oil and natural gas. Other prominent sectors include lumber, seafood and minerals.</span>
                                    </p></div>

                                <div id="content-below"><p><span id="more-8993"></span></p>

                                    <h3 id="3"><span style="font-weight: 400;">Cost of Studying and Living in Norway</span></h3>

                                    <p><span style="font-weight: 400;">Norway uses the Norwegian Krone (NOK) as its currency.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Public universities in Norway do not charge tuition fees even for international students. Depending on where you choose to study, you may be required to pay a small fee each semester, but that is normally between NOK 300 and NOK 600. This semester fee gets you membership to the student union, access to health services, counselling and sports facilities as well as an official student card. Your student card can reduce your fees on public transport and give you a discount for museums and art galleries. There are some courses at public institutions that you may have to pay for, but these are rare, and tend to be at postgraduate level. If you choose to study at a private institution, you will be required to pay tuition fees, and these will vary at each institution. There are some scholarships available for EU/EEA students and international students, make sure to check with your chosen institution about your eligibility.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Your living costs will depend on where you choose to live in Norway. The bigger cities will be more expensive than the smaller cities and towns. You may be able to apply to the Norwegian State Education Loan Fund for a grant to help you cover your costs. On average, you should budget for between NOK 9,500 and NOK 20,000 per month. Students from an EU/EEA country can get part-time work without any permission. Students from anywhere else will be required to apply for a work permit before you can get part-time work. No matter where you are from, you are permitted to work up to 20 hours per week during term time, and full-time during the holidays.</span>
                                    </p>

                                    <h3 id="4"><span style="font-weight: 400;">Visas</span></h3>

                                    <p><span style="font-weight: 400;">All students wishing to study at a Norwegian institution will need to obtain a visa, which comes in the form of a student residence permit. If you are from an EEA country, you do not need to apply for a residence permit before you arrive in Norway, but will need to do so within three months of your arrival. If you are from any other country, you will need to apply for a residence permit before you travel to Norway. This can be done at the Norwegian embassy or consulate in your home country. More information regarding student residence permits can be found on the </span><a href="https://www.udi.no/en/want-to-apply/studies/?resetguide=1"><span style="font-weight: 400;">Norwegian Directorate of Immigration (UDI) website</span></a><span style="font-weight: 400;">.</span></p>

                                    <p><span style="font-weight: 400;">Another thing that students need to consider is health insurance. If you are from a Nordic country, you will become a member of the Norwegian National Insurance Scheme if you are registered in the National Population Register. If you are from a Nordic country but are not registered, you are still entitled to health services under the National Insurance Act, and will not need an EHIC card. If you are from an EU/EEA country or Switzerland, you will not become a member of the Norwegian National Insurance Scheme, but will be able to access emergency and necessary healthcare so long as you have an EHIC card. If your country does not entitle you to an EHIC card, you will need to ensure you have health insurance for the duration of your stay. If you are from any other country, you will be automatically registered with the Norwegian National Health Insurance Scheme as long as your stay is one year or more. &nbsp;If you will not be in Norway for more than a year, you will need to make sure you have a valid health insurance policy. Some institutions have certain healthcare services on campus, to which you will have free access when you pay your semester fee. Check with your institution to determine if this is applicable to you.</span>
                                    </p>

                                    <h3 id="5"><span style="font-weight: 400;">Language</span></h3>

                                    <p><span style="font-weight: 400;">Norway has two official languages, and these each have variants. The first is Norwegian, with the variants being Bokmål and Nynorsk. The second is Sami, with the variants being Norther, Lule and Southern. As well as these, there are three official minority languages; Kven, Romani and Romanes.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">It is very common for degrees to be offered in Norwegian and English at institutions in Norway. A large majority of the population also speak good English. If you choose to study in a non-native language, you may have to provide evidence of your language proficiency. If you do not meet the language requirements, your institution may offer language courses to help you improve. Some degrees require you to have advanced knowledge of the Norwegian language, and these usually include a one-year language course for non-Norwegian speakers.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Even if you are able to study in English, you should still make the effort to learn as much Norwegian as possible. Communicating with locals and other students is the perfect way to practice. This is a skill that will make your everyday life easier, as well as looking great on your CV/resume!</span>
                                    </p>

                                    <h3 id="6"><span style="font-weight: 400;">OSLO</span></h3>



                                    <p><span style="font-weight: 400;">The capital city of Norway, Oslo is home to almost 680,000 people. The city was founded in 1040, and became the capital in around 1300. Since then, it has been destroyed in a fire in 1624, and was entirely rebuilt. It is now the economic and governmental centre, as well as hosting many of Norway’s higher education institutions. The city is culturally rich, and has many attractions for you to visit.</span>
                                    </p>

                                    <p><span style="font-weight: 400;">Located in the city there are several universities and university colleges. These include the University of Oslo, which is the oldest university in Norway. Between 1947 and 1989, the Nobel Peace Prize was awarded in the Atrium of the University of Oslo, which makes it the only university in the world to be involved in awarding of the Nobel Prize. Also located in the city is the BI Norwegian Business School, the Norwegian Academy of Music and the Norwegian University of Life Sciences, among many others.</span>
                                    </p></div>
