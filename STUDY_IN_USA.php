                                <br><h4>VISA PROCESS</h4><br>

                                <p>There are several immigrant and non-immigrant visa options at USA and one has to
                                    choose the one that is applicable according to the requirements. The passport
                                    must be valid for at least six months beyond the end of your intended stay in
                                    the US. Immigration regulations are very strict with respect to working while
                                    carrying a student visa. F-1 status, which is the most common status for
                                    full-time international students, allows for part time, on-campus employment
                                    (fewer than 20 hours per week.) J-1 student status allows for similar
                                    employment, with similar restrictions, as long as permission is given by the
                                    exchange visitor program sponsor. M-1 visa holders for technical and vocational
                                    programs are not permitted to work during the course of their studies.</p>

                                <p>To get the F-1 visa approved, the student will need to show that he/she has
                                    sufficient funds to pay for the first year of study and that you have resources
                                    available to cover the rest of your educational program. For an M-1 or J-1 visa,
                                    you will need to convince the authorities that you have sufficient funds to pay
                                    for all tuition and living costs for your complete stay in the US.</p>
                                <br> <h4>WORK OPPORTUNITIES</h4><br>


                                <p>Students are permitted to work 20 hours per week on-campus during the
                                    first years of study. In the second year students are allowed to take
                                    part-time jobs or co-op/internships off campus to fulfill academic or
                                    financial requirements. The on campus jobs do not pay much and certainly
                                    not enough to finance a university education. This kind of jobs can only
                                    be a supplement to other funds.</p>

                                <p>Along with the acceptability and recognition of education, USA also
                                    offers the best in terms of future job prospects and immigration
                                    possibilities after studies. All students are entitled to stay back and
                                    work for one year period in USA under OPT (Optional Professional
                                    Training). For STEM Graduates(Science, Technology, Engineering and
                                    Mathematics) the OPT can be further extended to another 24 months (Total
                                    36 Months). Students who are successful in finding a suitable placement
                                    can apply for H1B (Job Visa) during this period.</p><br>
