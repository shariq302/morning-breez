                                    <h4>Requirements</h4><br>
                                    <p>US Universities normally do not accept 3 years’ Bachelors degree from
                                        India like BA, B.Sc , B.Com for admission to their masters programs.
                                        Sixteen years of formal education or a four years’ Bachelors degree or
                                        Masters (MA/M. Com/MSC) with good grades (high first class or above) are
                                        the basic requirements for admission to MS programs in most US
                                        Universities. Apart from TOEFL (IELTS and PTE is also being accepted by
                                        many universities) students are also required to clear competitive exams
                                        like GRE/GMAT to secure admission and financial aid in good US
                                        Universities and also to secure visa without hassles.</p>
                                    <hr>
                                    <p><b>TOEFL</b><br>
                                        TOEFL is Test of English as a Foreign Language. The test is conducted by
                                        ETS (Educational Testing Service) and the test fee is $170.The score is
                                        valid for 2 years and the score is sent to 4 universities by ETS free of
                                        cost at the time of exam. ETS charges additionally for scores sent to
                                        universities later. For more info visit the official website at
                                        www.toefl.org or www.ets.org</p>
                                    <hr>
                                    <p><b>GRE</b><br>
                                        GRE is Graduate Record Examinations and is administered by ETS
                                        (Educational Testing Service) The test is essential for candidates
                                        seeking admission in a Masters Degree program in fields other than
                                        management are required to take the GRE. Most Universities require GRE
                                        for PhD. programs. The score of GRE has been generally valid for 5 years
                                        and the score is sent to 4 universities free of cost at the time of GRE
                                        exam. ETS charges fees for scores sent to universities later.</p>
                                    <hr>
                                    <p><b>GMAT</b><br>
                                        GMAT refers Graduate Management Admission Test which is administered by
                                        Graduate management Council (GMAC) The test is required for admission
                                        into a MBA Program. The test fee cost $250. The score is generally valid
                                        for 5 years and the score is sent to 5 universities by ETS free of cost
                                        at the time of exam. Students can register for the test by phone, fax or
                                        online and visit the official website at www.gmat.com for more info.</p>
                                    <hr>
                                    <p><b>SAT</b><br>
                                        Scholastic Aptitude Test is referred as SAT. The test is administered by
                                        the College Board and is required for admission into any Bachelors
                                        Degree Program. SAT is administrated by the College Board and offered 7
                                        times a year. The score is sent to four universities free of cost, which
                                        can be selected at any time of registering for the exam. College Board
                                        charges additional for the score to be sent to university later. For
                                        more info visit the official website www.collegeboard.com</p>
                                    <hr>
                                    <p><b>TSE</b><br>
                                        TSE is Test of Spoken English conducted by ETS (Educational Testing
                                        Service) The validity of the score is 2 years. The test has the duration
                                        of 20 minutes and TSE is conducted once in a month. The score in sent to
                                        2 Universities by ETS free of cost. ETS charges additionally for scores
                                        sent to more than 2 Universities. For more info visit the official
                                        website atwww.toefl.org or www.ets.org</p>
