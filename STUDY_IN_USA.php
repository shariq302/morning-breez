                                    recommended that You apply as early as possible. Applications can be initiated
                                    during final year/semester study of your course (students pursuing BSc/B.com can
                                    start applying as soon as you receive your S4/S5 mark lists and B.Tech students
                                    can start applying as soon as you get your s6 mark-lists. High school students
                                    (Higher secondary) can start applying with your 11th grades or projected grades
