    <h2>Why Study AUSTRALIA</h2>


    <p>Geographically Australia situates 11,000 Km southwest of North America and 3,200
        kilometers southeast of mainland Asia. The largest island and the smallest continent in
        the world.</p>

    <p>Australia is blessed with temperate climate and the highlands and tablelands of Tasmania
        and the south eastern corner of the mainland are the coldest areas. It is a land of
        bright sunshine, surf beaches, tropical rivers, rainforests, mountain ranges, red
        deserts and rich green coastal plains. Summer is from December to February; autumn from
        March to May; winter from June to August; and spring from September to November.</p>

    <p>Australia is country of dynamic multiculturalism with invaluable contributions from
        migrants from all parts of the world. Australia owns a rich tradition of art and
        culture. Australians provide strong support towards films, music, theater, painting and
        a range of other fine arts forms. They are also a strong global presence regarding
        sports.</p>

    <p>The transportation options at Australia are wide and well equipped. It has an extensive
        public transport system that includes trains, buses, tramways, ferries, two major
        national airlines and a number of regional airlines. Metropolitan areas are divided into
        zones and your ticket type and cost depends on which zone you are going to travel in and
        for how long. Tickets are sold at train stations, on buses and trams and at news
        agencies.</p>


    <p>Australia’s system of education is of the highest standard and enjoys international
        renown. Australia is widely recognized as a world leader in the field of international
        education and English language training.</p>
